public class Solution {

    public int sum(int a, int b) {
        /*
         * Compute the sum of a and b
         */
        return a+b;
    }

    public int diff(int a, int b) {
        /*
         * Compute the difference of a and b
         */
        return a-b;
    }

    public int prod(int a, int b) {
        /*
         * Compute the product of a and b
         */
        return a*b;
    }
}
